#include "libft.h"

int	ft_tolower(int c)
{
	if (c > 64 && c < 91)
		c = c + 32;
	return (c);
}

int	ft_alltolower(char *s)
{
	int i;

	i = -1;
	while (s[++i] != '\0')
		s[i] = ft_tolower(s[i]);
	return (0);
}
